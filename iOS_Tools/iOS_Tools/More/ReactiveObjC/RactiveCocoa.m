#import "RactiveCocoa.h"

@implementation RactiveCocoa
/*
 在RactiveCocoa中,万物皆信号
 
 RactiveCocoa是GitHub中的一个开源框架,能够通过信号提供大量方便的时间处理方案.
 让我们更简单粗暴地去处理事件.
 
 其中分为RactiveObjc(OC)和ReactiveSwift(Swift)
 
 在团队协作开发中,必须注意到一个点:
 对于实习RAC的人来说,使用RAC是非常方便的,但是对于不熟悉RAC的人来说,由于RAC的可读性是很差的,所以需要耗费时间阅读和学习
 
 还需要注意的是,RAC需要大量的block,未避免循环引用,是要使用@weakify(self)和@strongify(self)

 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 */

